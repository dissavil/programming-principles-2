# if elif else statement

# Example 1
x = 0
if x > 0:
    print("Positive")
elif x < 0:
    print("Negative")
else:
    print("Zero")

# Example 2
score = 85
if score >= 90:
    print("A")
elif score >= 80:
    print("B")
else:
    print("C")

# Example 3
day = 3
if day == 1:
    print("Monday")
elif day == 2:
    print("Tuesday")
else:
    print("Another day")

# Example 4
age = 65
if age < 18:
    print("Child")
elif age < 60:
    print("Adult")
else:
    print("Senior")

# Example 5
temp = 25
if temp > 30:
    print("Hot")
elif temp > 15:
    print("Warm")
else:
    print("Cold")

# if else statement

# Example 1
x = 5
if x > 10:
    print("Greater than 10")
else:
    print("10 or less")

# Example 2
age = 16
if age >= 18:
    print("Adult")
else:
    print("Minor")

# Example 3
num = 0
if num > 0:
    print("Positive")
else:
    print("Zero or negative")

# Example 4
logged_in = False
if logged_in:
    print("Welcome")
else:
    print("Please log in")

# Example 5
a = 7
b = 7
if a == b:
    print("Equal")
else:
    print("Not equal")

# Short hand if else

# Example 1
a = 10
b = 5
print("a is greater") if a > b else print("b is greater")

# Example 2
x = 7
print("Even") if x % 2 == 0 else print("Odd")

# Example 3
age = 18
status = "Adult" if age >= 18 else "Minor"
print(status)

# Example 4
logged = False
print("Yes") if logged else print("No")

# Example 5
a = 3
b = 3
print("Equal") if a == b else print("Not equal")

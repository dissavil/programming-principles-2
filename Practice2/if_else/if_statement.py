# Simple if statement

# Example 1
x = 10
if x > 5:
    print("x is greater than 5")

# Example 2
a = 3
if a == 3:
    print("a equals 3")

# Example 3
age = 18
if age >= 18:
    print("Adult")

# Example 4
num = -1
if num < 0:
    print("Negative number")

# Example 5
flag = True
if flag:
    print("Flag is true")

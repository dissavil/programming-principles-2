# While loop

# Example 1
i = 1
while i <= 5:
    print(i)
    i += 1

# Example 2
count = 0
while count < 3:
    print("Hello")
    count += 1

# Example 3
x = 5
while x > 0:
    print(x)
    x -= 1

# Example 4
num = 1
while num <= 10:
    print(num * 2)
    num += 1

# Example 5
a = 0
while a < 5:
    print("Loop")
    a += 1

# For loop with break

# Example 1
for i in range(5):
    if i == 3:
        break
    print(i)

# Example 2
for ch in "Python":
    if ch == "h":
        break
    print(ch)

# Example 3
for x in range(10):
    if x == 5:
        break
    print(x)

# Example 4
for i in [1, 2, 3, 4]:
    if i == 3:
        break
    print(i)

# Example 5
for i in range(1, 10):
    if i > 6:
        break
    print(i)
